  <h2>Tujuan</h2>
  <p>Program ini bertujuan meningkatkan motivasi dan memberi penghargaan
kepada Dosen yang telah berhasil menghasilkan luaran Kekayaan Intelektual
(KI) dari hasil penelitiannya baik berupa Hak Cipta maupun Paten, sehingga 
meningkatkan KI khususnya di Polinema.
</p>
<br>

<h2>Jumlah Intensif</h2>
<p>Insentif KI diberikan kepada dosen terbagi menjadi dua yaitu
<li>Insentif Hak Cipta sebesar sebesar Rp 1.500.000 (satu juta lima ratus ribu 
rupiah). </li>
<li>Insentif Paten/Paten Sederhana sebesar Rp 3.000.000 (tiga juta rupiah).
Anggaran insentif KI dibebankan pada DIPA Politeknik Negeri Malang.
</li>
</p>
<br>


<h2>Persyaratan</h2>
<p>
<li>Insentif KI dibedakan untuk kategori sebagai berikut:</li> 
A. Paten; <br>
B. Paten Sederhana;<br>
C. Hak Cipta;<br>
D. Kekayaan Intelektual lainnya. <br>
Usulan insentif KI dapat diproses jika pengusul dan KInya memenuhi syarat 
sebagai berikut


<li>Insentif kategori A & B diberikan kepada Dosen yang mendaftarkan 
ciptaannya dan sudah mendapatkan nomor pendaftaran, serta ciptaan yang 
didaftarkan tersebut dinyatakan lolos pemeriksaan kelengkapan dokumen.
</li>

<li>Insentif kategori C & D diberikan kepada Dosen yang mendaftarakan PANDUAN PENGAJUAN INSENTIF KI 9
ciptaannya dan sudah mendapatkan sertifikat ciptaannya. </li>

<li>KI yang diberikan insentif adalah KI yang didaftarkan oleh Dosen Polinema 
dengan pengalihan hak pada Polinema.
</li>

<li>Pengusul adalah Dosen Polinema yang memiliki NIDN.</li>

<li>KI yang diajukan untuk mendapatkan insentif belum pernah mendapatkan 
insentif yang sama di Polinema.</li>

<li>KI yang dapat diajukan untuk mendapatkan insentif harus didaftarkan melalui 
Sentra KI UPT P2M Polinema</li>
</p>
<br>

<h2>Mekanisme Pengusulan</h2>
<p>Insentif KI akan diberikan dengan mengikuti prosedur berikut.
<li>Pengusul wajib mengisi usulan insentif kekayaan intelektual secara online</li>
<li>Berkas-berkas softcopy (scan) yang dibutuhkan dalam pengusulan insentif 
publikasi ilmiah tersebut antara lain: <br>
1. Surat Pernyataan (Lampiran 1) <br>
2. Identitas usulan insentif KI (Lampiran 2) 
<li>Pengusulan akan diproses hanya jika pengusul telah registrasi secara online
melalui laman https://bit.ly/InsentifKI atau https:// ppm.polinema.ac.id. </li>
</li>
<li>Pencairan insentif KI melalui proses verifikasi oleh UPT P2M.</li>
</p>
